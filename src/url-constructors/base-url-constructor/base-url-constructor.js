/**
 * @module url-constructors/base-url-constructor
 * @description - This module contains functions that return the base url for sports-reference.com sites
 */

/**
 * @returns {string} - The base url for pro football reference
 */
export function getProFootballUrl () {
    return 'https://www.pro-football-reference.com/'
}
/**
 * @returns {string} - The base url for pro basketball reference
 */
export function getProBasketballUrl () {
    return 'https://www.basketball-reference.com/'
}
